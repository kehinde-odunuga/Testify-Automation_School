# What is the difference between a list and a dictionary?

# A List is a collection of index values pairs as that of an array.
# The elements are accessed through indices.
# The order of the elements entered are maintained.
# List is created by placing elements in [ ] separated by commas “,“

colours = ['red', 'green', 'blue', 'white', 'black']
print('List:', colours)



# A Dictionary is a hashed structure of key and value pairs.
# There is no guarantee for maintaining order.
# The elements are accessed through key-values.
# Dictionary is created by placing elements in { } as “key”:”value”, each key value pair is separated by commas “, “

fruits = {
    "mango": "green",
    "apple": "red",
    "pawpaw": "yellow",
    "cocnut": "brown"
}
print('Dictionary:', fruits)